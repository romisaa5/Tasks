# List — مرتبة، قابلة للتغيير، تقبل تكرار
lst = [1, 2, 3, 2]       # [] أقواس مربعة

# Tuple — مرتبة، غير قابلة للتغيير
tup = (1, 2, 3)            # () أقواس دايرية

# Set — غير مرتبة، لا تكرار
st = {"a", "b", "c"}       # {} أقواس معقوفة

# Dictionary — مفتاح : قيمة
d = {"name": "Ali", "age": 20}
print(d["name"])           # Ali